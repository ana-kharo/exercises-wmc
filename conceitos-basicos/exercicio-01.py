# Primeiro irei pedir os numeros para as pessoas usuárias
numero1 = float(input('Favor, digite o primeiro número: '))
numero2 = float(input('Agora, digite o segundo número: '))

# Depois irei realizar as operações
soma = numero1 + numero2
subtracao = numero1 - numero2
multiplicacao = numero1 * numero2
divisao = numero1 / numero2

# Agora irei mostrar os resultados de cada operação
print(f'A soma dos números digitados é: {soma}')
print(f'A subtração dos números digitados é: {subtracao}')
print(f'A multiplicação dos números digitados é: {multiplicacao}')
print(f'A divisão dos números digitados é: {divisao}')

